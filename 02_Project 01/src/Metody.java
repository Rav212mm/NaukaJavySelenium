public class Metody {

    // Metoda bez parametrów
    public void powitaj() {
        System.out.println("Witaj użytkowniku!");
    }

    // Metoda z parametrem
    public void wyswietlTekst(String tekst) {
        System.out.println("Wprowadzony tekst: " + tekst);
    }

    // Metoda zwracająca wartość (dodawanie)
    public int dodaj(int a, int b) {
        return a + b;
    }

}
